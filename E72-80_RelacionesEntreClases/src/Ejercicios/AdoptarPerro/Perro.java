package Ejercicios.AdoptarPerro;

public class Perro {

    String nombre;
    String raza;
    int edad;
    String tamanio;

    public Perro(String nombre, String raza, int edad, String tamanio) {
        this.nombre = nombre;
        this.raza = raza;
        this.edad = edad;
        this.tamanio = tamanio;
    }
}
